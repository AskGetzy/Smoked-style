export type TranslationDict = (typeof translations)[Language]
export type TranslationKey = keyof TranslationDict

export function getTranslation(lang: Language): TranslationDict {
  return translations[lang]
}

const STATUS_KEYS: Record<string, keyof TranslationDict> = {
  pending: 'pending',
  approved: 'approved',
  out_for_delivery: 'outForDelivery',
  delivered: 'delivered',
  cancelled: 'cancelled',
  ready_for_pickup: 'readyForPickup',
  payment_failed: 'paymentFailed',
}

export function orderStatusLabel(t: TranslationDict, status: string): string {
  const key = STATUS_KEYS[status]
  return key ? t[key] : status.replace(/_/g, ' ')
}

const CATEGORY_KEYS: Record<string, keyof TranslationDict> = {
  jerky: 'jerky',
  steaks: 'steaks',
  smoked: 'smoked',
  non_smoked: 'nonSmoked',
  boards: 'boards',
}

export function productCategoryLabel(t: TranslationDict, category: string): string {
  const key = CATEGORY_KEYS[category]
  return key ? t[key] : category.replace(/_/g, ' ')
}
